def plot_forecast(city):
    FORECAST_URL = f"https://api.openweathermap.org/data/2.5/forecast?q={city}&appid={API_KEY}&units=metric"
    response = requests.get(FORECAST_URL)

    if response.status_code != 200:
        print(f"âŒ Failed to retrieve forecast: {response.status_code} - {response.text}")
        return

    data = response.json()
    dates, temperatures, humidities = [], [], []

    for entry in data['list']:
        dt_txt = entry['dt_txt']
        temp = entry['main']['temp']
        humidity = entry['main']['humidity']
        dates.append(datetime.strptime(dt_txt, "%Y-%m-%d %H:%M:%S"))
        temperatures.append(temp)
        humidities.append(humidity)

    df = pd.DataFrame({
        'DateTime': dates,
        'Temperature (Â°C)': temperatures,
        'Humidity (%)': humidities
    })

    # ğŸŒ¡ï¸ Temperature Plot
    plt.figure(figsize=(14, 5))
    plt.plot(df['DateTime'], df['Temperature (Â°C)'], color='crimson', marker='o', linestyle='--')
    plt.title(f"ğŸŒ¡ï¸ Temperature Forecast for {city.title()}", fontsize=14)
    plt.xlabel("Date & Time")
    plt.ylabel("Temperature (Â°C)")
    plt.xticks(rotation=45)
    plt.grid(True)

    max_temp = max(temperatures)
    min_temp = min(temperatures)
    max_time = dates[temperatures.index(max_temp)]
    min_time = dates[temperatures.index(min_temp)]

    plt.annotate(f'Max: {max_temp}Â°C', xy=(max_time, max_temp), xytext=(max_time, max_temp + 1),
                 arrowprops=dict(arrowstyle="->", color='red'), color='red')
    plt.annotate(f'Min: {min_temp}Â°C', xy=(min_time, min_temp), xytext=(min_time, min_temp - 2),
                 arrowprops=dict(arrowstyle="->", color='blue'), color='blue')

    plt.tight_layout()
    plt.show()

    # ğŸ’§ Humidity Plot
    plt.figure(figsize=(14, 5))
    plt.plot(df['DateTime'], df['Humidity (%)'], color='royalblue', marker='x', linestyle='-')
    plt.title(f"ğŸ’§ Humidity Forecast for {city.title()}", fontsize=14)
    plt.xlabel("Date & Time")
    plt.ylabel("Humidity (%)")
    plt.xticks(rotation=45)
    plt.grid(True)
    plt.tight_layout()
    plt.show()

    df.to_csv(f"{city}_5_day_forecast.csv", index=False)
    print(f"ğŸ“ Forecast data saved to '{city}_5_day_forecast.csv'.")

